package com.mycompany.operatorpenugasan;

/**
 * Demonstrasi Operator Penugasan dalam Java
 * @author hanif
 * @contributor luqmanaru
 */
public class OperatorPenugasan {

    public static void main(String[] args) {
        int a;
        int b;
        
        // Pengisian nilai awal
        a = 7;
        b = 56;
        
        // Operator penugasan perkalian
        b *= a;  // Sama dengan b = b * a
        System.out.println("Hasil perkalian: " + b);
    }
}
